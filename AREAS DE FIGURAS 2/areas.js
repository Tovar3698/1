function areaCuadrado(Plado){
    let lado = Plado; 
    let resultado1 = lado * lado;
                            
    return resultado1;
}
function areaRectangulo(Plado2,Paltura){
    let lado = Plado2; 
    let altura = Paltura
    let resultado2 = lado * altura;
                            
    return resultado2;
}
function areaTriangulo(Plado3,Paltura){
    let lado = Plado3; 
    let altura = Paltura
    let resultado3 = (lado * altura)/2;
                            
    return resultado3;
}


