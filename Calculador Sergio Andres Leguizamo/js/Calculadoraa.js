function suma(num1, num2) {
    let sumar = num1+num2;
    return sumar;
}

function resta(num1,num2){

    let restar = num1 - num2;
    return restar;

}
function multiplicacion(num1,num2){
    
    let multiplicar = num1 * num2;
    return multiplicar;
}
function division(num1,num2){
    
    let dividir = num1 / num2;
    return dividir;
}